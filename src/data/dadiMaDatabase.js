export function searchDadiMaDB(query, chatCategory) {
  if (!query) return null;
  const normalizedQuery = query.toLowerCase().trim();
  
  // Filter by category if specified and not 'all'
  const targetCategory = chatCategory && chatCategory !== 'all' ? chatCategory.toLowerCase().trim() : null;
  const pool = targetCategory 
    ? DADI_MA_DB.filter(entry => entry.category.toLowerCase() === targetCategory)
    : DADI_MA_DB;

  let bestMatch = null;
  let highestScore = 0;

  for (const entry of pool) {
    let score = 0;
    
    // Check exact question
    if (entry.question.toLowerCase() === normalizedQuery) {
      score += 100;
    } else if (entry.question.toLowerCase().includes(normalizedQuery)) {
      score += 40;
    }

    // Check keywords (exact or partial)
    for (const keyword of entry.keywords) {
      const lowerKeyword = keyword.toLowerCase();
      if (lowerKeyword === normalizedQuery) {
        score += 80;
      } else if (lowerKeyword.includes(normalizedQuery) || normalizedQuery.includes(lowerKeyword)) {
        score += 30;
      }
    }

    // Check ID
    if (entry.id.toLowerCase() === normalizedQuery) {
      score += 100;
    }

    if (score > highestScore) {
      highestScore = score;
      bestMatch = entry;
    }
  }

  // Only return if we have a reasonable match score
  return highestScore > 0 ? bestMatch : null;
}

