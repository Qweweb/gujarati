// Simple deterministic date-seeded pseudo-random number generator
function getSeededRandom(seed) {
  const x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
}

function getSeededItem(array, seed, offset) {
  const randVal = getSeededRandom(seed + offset);
  const index = Math.floor(randVal * array.length);
  return array[index];
}

