export function generateDailyRashifal(rashiId, dateObj = new Date()) {
  // Generate a unique seed based on the date and Rashi id
  const rashiIndex = RASHI_DATA.findIndex(r => r.id === rashiId);
  const rashiOffset = rashiIndex !== -1 ? rashiIndex : 0;
  
  const day = dateObj.getDate();
  const month = dateObj.getMonth() + 1;
  const year = dateObj.getFullYear();
  
  // Deterministic daily seed: YYYYMMDD + rashi offset
  const dateSeed = (year * 10000) + (month * 100) + day + rashiOffset;
  
  const career = getSeededItem(CAREER_PREDICTIONS, dateSeed, 100);
  const finance = getSeededItem(FINANCE_PREDICTIONS, dateSeed, 200);
  const family = getSeededItem(FAMILY_PREDICTIONS, dateSeed, 300);
  const health = getSeededItem(HEALTH_PREDICTIONS, dateSeed, 400);
  const remedy = getSeededItem(REMEDY_PREDICTIONS, dateSeed, 500);
  const luckyColor = getSeededItem(LUCKY_COLORS, dateSeed, 600);
  const luckyNumber = getSeededItem(LUCKY_NUMBERS, dateSeed, 700);

  return {
    career,
    finance,
    family,
    health,
    remedy,
    luckyColor,
    luckyNumber
  };
}
