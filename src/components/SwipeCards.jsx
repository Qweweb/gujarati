export default function SwipeCards() {
  const navigate = useNavigate();

  const getTempleIdFromCardId = (cardId) => {
    const mapping = {
      "f_somnath": "somnath",
      "f_nageshwar": "nageshwar",
      "f_koteshwar": "koteshwar",
      "f_nishkalank": "nishkalank",
      "f_dwarkadhish": "dwarkadhish",
      "f_dakor": "dakor",
      "f_shamlaji": "shamlaji",
      "f_bhalka": "bhalkatirth",
      "f_ambaji": "ambaji",
      "f_pavagadh": "pavagadh",
      "f_bahucharaji": "bahucharaji",
      "f_chotila": "chotila",
      "f_khodaldham": "khodiyarmata",
      "f_modherasun": "modhera_sun",
      "f_akshardham_gnd": "akshardham",
      "f_palitana_jain": "palitana",
      "f_girnar_jain": "girnar_jain",
      "f_taranga_jain": "taranga",
      "f_shankheshwar_jain": "shankheshwar",
      "f_virpur_jalaram": "virpur",
      "f_satadhar_dham": "satadhar",
      "f_sarangpur_hanuman": "sarangpur_hanuman",
      "f_bhadkeshwar_sea": "bhadkeshwar"
    };
    return mapping[cardId] || null;
  };

  const [cards, setCards] = useState(FACT_CARDS);
  const [currentIndex, setCurrentIndex] = useState(0);
  const [wishlist, setWishlist] = useState([]);
  
  // Audio state
  const [isPlayingAudio, setIsPlayingAudio] = useState(false);
  const [audioSupported, setAudioSupported] = useState(false);
  
  // Dragging states
  const [dragStart, setDragStart] = useState({ x: 0, y: 0 });
  const [dragOffset, setDragOffset] = useState({ x: 0, y: 0 });
  const [isDragging, setIsDragging] = useState(false);
  
  const currentCard = cards[currentIndex];

  useEffect(() => {
    // Load wishlist from localStorage
    const savedWishlist = JSON.parse(localStorage.getItem('otlo_wishlist') || '[]');
    setWishlist(savedWishlist);

    if ('speechSynthesis' in window) {
      setAudioSupported(true);
    }

    return () => {
      if ('speechSynthesis' in window) {
        window.speechSynthesis.cancel();
      }
    };
  }, []);

  const playAudio = (text) => {
    if (!audioSupported) return;
    
    window.speechSynthesis.cancel();
    const utterance = new SpeechSynthesisUtterance(text);
    const voices = window.speechSynthesis.getVoices();
    const guVoice = voices.find(v => v.lang.includes('gu') || v.lang.includes('hi'));
    if (guVoice) {
      utterance.voice = guVoice;
    }
    utterance.lang = 'gu-IN';
    utterance.rate = 0.9;
    
    utterance.onstart = () => setIsPlayingAudio(true);
    utterance.onend = () => setIsPlayingAudio(false);
    utterance.onerror = () => setIsPlayingAudio(false);
    
    window.speechSynthesis.speak(utterance);
  };

  const stopAudio = () => {
    if ('speechSynthesis' in window) {
      window.speechSynthesis.cancel();
    }
    setIsPlayingAudio(false);
  };

  const handleNextCard = () => {
    stopAudio();
    setDragOffset({ x: 0, y: 0 });
    setCurrentIndex(prev => prev + 1);
  };

  const handleLike = (card) => {
    const updated = [...wishlist];
    if (!updated.some(item => item.id === card.id)) {
      updated.push(card);
      localStorage.setItem('otlo_wishlist', JSON.stringify(updated));
      setWishlist(updated);
      
      // Award Coins
      const coins = parseInt(localStorage.getItem('gujarat_coins') || '0', 10);
      localStorage.setItem('gujarat_coins', (coins + 5).toString());
      window.dispatchEvent(new Event('coins-updated'));
      window.dispatchEvent(new CustomEvent('show-toast', { detail: { message: `❤️ ${card.name_gu} તમારી ડાયરીમાં ઉમેરાઈ ગયું! (+૫ કોઈન્સ)` } }));
    }
    handleNextCard();
  };

  const handleSkip = () => {
    handleNextCard();
  };

  // Dragging Handlers
  const handleStart = (clientX, clientY) => {
    if (currentIndex >= cards.length) return;
    setIsDragging(true);
    setDragStart({ x: clientX, y: clientY });
  };

  const handleMove = (clientX, clientY) => {
    if (!isDragging) return;
    const dx = clientX - dragStart.x;
    const dy = clientY - dragStart.y;
    setDragOffset({ x: dx, y: dy });
  };

  const handleEnd = () => {
    if (!isDragging) return;
    setIsDragging(false);

    // Swipe Thresholds
    const threshold = 120;
    if (dragOffset.x > threshold) {
      // Swiped Right -> Like
      handleLike(currentCard);
    } else if (dragOffset.x < -threshold) {
      // Swiped Left -> Skip
      handleSkip();
    } else {
      // Reset
      setDragOffset({ x: 0, y: 0 });
    }
  };

  // Mouse Events
  const onMouseDown = (e) => handleStart(e.clientX, e.clientY);
  const onMouseMove = (e) => handleMove(e.clientX, e.clientY);
  const onMouseUp = () => handleEnd();

  // Touch Events
  const onTouchStart = (e) => {
    if (e.touches.length > 0) {
      handleStart(e.touches[0].clientX, e.touches[0].clientY);
    }
  };
  const onTouchMove = (e) => {
    if (e.touches.length > 0) {
      handleMove(e.touches[0].clientX, e.touches[0].clientY);
    }
  };
  const onTouchEnd = () => handleEnd();

  // Wishlist removal
  const removeFromWishlist = (id) => {
    const updated = wishlist.filter(item => item.id !== id);
    localStorage.setItem('otlo_wishlist', JSON.stringify(updated));
    setWishlist(updated);
  };

  // Reset deck
  const handleResetDeck = () => {
    setCurrentIndex(0);
    setDragOffset({ x: 0, y: 0 });
  };

  const swipeRotation = dragOffset.x * 0.05; // degree of rotation as you drag
  const swipeOpacity = Math.max(0.6, 1 - Math.abs(dragOffset.x) / 300);

  return (
    <div className="animate-fade-in space-y-8 pb-12">
      {/* Page Header */}
      <div className="space-y-1">
        <h2 className="font-gujarati font-black text-4xl text-primary">જ્ઞાન કાર્ડ્સ (Gujarat Swipes) 🎴</h2>
        <p className="font-gujarati text-outline text-lg">ડાબી બાજુ સ્વાઇપ કરીને આગળ વધો અને જમણી બાજુ સ્વાઇપ કરી તમારી ટ્રાવેલ ડાયરીમાં જોડો.</p>
      </div>

      {/* Card Swipe Section */}
      <div className="flex flex-col items-center justify-center min-h-[500px] relative select-none">
        {currentIndex < cards.length ? (
          <div className="relative w-[320px] h-[460px] flex items-center justify-center">
            
            {/* Background stacked card effect */}
            {currentIndex + 1 < cards.length && (
              <div 
                className="absolute inset-0 rounded-[2.5rem] bg-stone-200 dark:bg-stone-800 scale-95 translate-y-4 border border-stone-300 dark:border-stone-700 shadow-sm opacity-55 pointer-events-none"
                style={{ zIndex: 10 }}
              />
            )}
            
            {/* Active Interactive Card */}
            <div
              onMouseDown={onMouseDown}
              onMouseMove={onMouseMove}
              onMouseUp={onMouseUp}
              onMouseLeave={onMouseUp}
              onTouchStart={onTouchStart}
              onTouchMove={onTouchMove}
              onTouchEnd={onTouchEnd}
              style={{
                transform: `translate(${dragOffset.x}px, ${dragOffset.y}px) rotate(${swipeRotation}deg)`,
                opacity: swipeOpacity,
                zIndex: 20,
                transition: isDragging ? 'none' : 'transform 0.4s cubic-bezier(0.16, 1, 0.3, 1), opacity 0.4s ease'
              }}
              className={`absolute inset-0 rounded-[2.5rem] bg-gradient-to-br ${currentCard.gradient} p-8 text-white shadow-2xl flex flex-col justify-between cursor-grab active:cursor-grabbing overflow-hidden border-2 border-white/10`}
            >
              {/* Overlay badges for Dragging Direction */}
              {dragOffset.x > 30 && (
                <div 
                  className="absolute top-6 left-6 border-4 border-emerald-400 bg-emerald-500/80 text-emerald-100 rounded-2xl px-4 py-2 font-gujarati font-black text-lg rotate-[-12deg] z-30 uppercase tracking-widest shadow-lg"
                  style={{ opacity: Math.min(1, dragOffset.x / 100) }}
                >
                  ગમ્યું ❤️
                </div>
              )}
              {dragOffset.x < -30 && (
                <div 
                  className="absolute top-6 right-6 border-4 border-rose-400 bg-rose-500/80 text-rose-100 rounded-2xl px-4 py-2 font-gujarati font-black text-lg rotate-[12deg] z-30 uppercase tracking-widest shadow-lg"
                  style={{ opacity: Math.min(1, -dragOffset.x / 100) }}
                >
                  આગળ વધો ❌
                </div>
              )}

              {/* Decorative Card Texture */}
              <div className="absolute inset-0 bg-[radial-gradient(circle_at_top,_var(--tw-gradient-stops))] from-white/10 via-transparent to-transparent pointer-events-none"></div>

              {/* Card Header details */}
              <div className="flex justify-between items-start z-10 gap-2">
                <div className="flex flex-col gap-1.5 items-start">
                  <span className="bg-white/20 border border-white/35 px-4 py-1.5 rounded-full text-xs font-gujarati font-bold">
                    {currentCard.location}
                  </span>
                  {getTempleIdFromCardId(currentCard.id) && (
                    <button
                      onClick={(e) => {
                        e.stopPropagation();
                        const templeId = getTempleIdFromCardId(currentCard.id);
                        navigate(`/devotional?section=temples&templeId=${templeId}`);
                      }}
                      className="bg-amber-400 hover:bg-amber-300 text-amber-950 px-3.5 py-1 rounded-full text-[10px] font-gujarati font-black flex items-center gap-1 shadow-md hover:scale-105 active:scale-95 transition-all border border-amber-300/40"
                    >
                      <span>મંદિર દર્શન</span>
                      <span className="material-symbols-outlined text-[10px]">open_in_new</span>
                    </button>
                  )}
                </div>
                <span className="text-4xl filter drop-shadow-md select-none">{currentCard.icon}</span>
              </div>

              {/* Central Title/Text Content */}
              <div className="space-y-4 z-10">
                <div className="space-y-0.5">
                  <h3 className="font-gujarati font-black text-3xl tracking-wide">{currentCard.name_gu}</h3>
                  <p className="font-gujarati text-xs text-white/70 font-semibold italic">({currentCard.name_en})</p>
                </div>
                <p className="font-gujarati font-bold text-sm text-white/90 leading-relaxed max-w-sm">
                  {currentCard.desc}
                </p>
              </div>

              {/* Interactive buttons row */}
              <div className="flex justify-between items-center gap-4 z-10 pointer-events-auto">
                <button
                  onClick={(e) => { e.stopPropagation(); handleSkip(); }}
                  className="h-14 w-14 rounded-full bg-white/10 hover:bg-white/20 text-white flex items-center justify-center border border-white/20 active:scale-90 transition-transform shadow-inner"
                  title="નથી ગમ્યું"
                >
                  <span className="material-symbols-outlined text-2xl font-bold">close</span>
                </button>

                {audioSupported && (
                  isPlayingAudio ? (
                    <button
                      onClick={(e) => { e.stopPropagation(); stopAudio(); }}
                      className="h-16 w-16 rounded-full bg-red-500 hover:bg-red-400 text-white flex items-center justify-center shadow-lg active:scale-90 transition-transform border-2 border-white/20"
                      title="ઓડિયો અટકાવો"
                    >
                      <span className="material-symbols-outlined text-3xl">volume_off</span>
                    </button>
                  ) : (
                    <button
                      onClick={(e) => { e.stopPropagation(); playAudio(currentCard.audioText); }}
                      className="h-16 w-16 rounded-full bg-white text-primary flex items-center justify-center shadow-lg active:scale-90 transition-transform hover:scale-105"
                      title="ઓડિયો સાંભળો"
                    >
                      <span className="material-symbols-outlined text-3xl">volume_up</span>
                    </button>
                  )
                )}

                <button
                  onClick={(e) => { e.stopPropagation(); handleLike(currentCard); }}
                  className="h-14 w-14 rounded-full bg-white text-rose-500 hover:bg-rose-50 flex items-center justify-center active:scale-90 transition-transform shadow-lg"
                  title="વિશલિસ્ટ કરો"
                >
                  <span className="material-symbols-outlined text-2xl" style={{ fontVariationSettings: "'FILL' 1" }}>favorite</span>
                </button>
              </div>

            </div>
          </div>
        ) : (
          <div className="bg-white dark:bg-stone-900 border border-stone-200 dark:border-stone-850 p-12 rounded-[2.5rem] text-center max-w-sm w-full flex flex-col items-center justify-center gap-6 shadow-sm">
            <span className="material-symbols-outlined text-6xl text-amber-500 animate-pulse">style</span>
            <div>
              <h4 className="font-gujarati font-black text-xl text-on-surface">બધા કાર્ડ્સ પૂરા થઈ ગયા! 🎉</h4>
              <p className="font-gujarati text-xs text-stone-400 mt-2 leading-relaxed">
                અભિનંદન! તમે ગુજરાતની ઘણી અવનવી વાતો જાણી લીધી છે. ફરીથી રમવા માટે નીચેનું બટન દબાવો.
              </p>
            </div>
            <button
              onClick={handleResetDeck}
              className="w-full bg-gradient-to-r from-amber-500 to-orange-500 hover:from-amber-400 hover:to-orange-400 text-white py-3.5 rounded-2xl font-gujarati font-black text-sm active:scale-95 transition-transform"
            >
              શરૂઆતથી ફરી જુઓ ↺
            </button>
          </div>
        )}
      </div>

      {/* Wishlist Section */}
      <section className="space-y-4">
        <div className="flex items-center gap-2 px-2">
          <span className="material-symbols-outlined text-rose-500" style={{ fontVariationSettings: "'FILL' 1" }}>favorite</span>
          <h4 className="font-gujarati font-black text-2xl text-on-surface">મારી ટ્રાવેલ ડાયરી (Travel Wishlist)</h4>
        </div>

        {wishlist.length === 0 ? (
          <div className="bg-stone-50 dark:bg-stone-950 border border-stone-200/50 dark:border-stone-850 border-dashed p-8 rounded-[2rem] text-center text-stone-400 dark:text-stone-600 text-xs">
            <p className="font-gujarati">તમારી મનપસંદ જગ્યાઓને અહીં સેવ કરવા કાર્ડ્સને જમણી બાજુ સ્વાઇપ કરો!</p>
          </div>
        ) : (
          <div className="grid grid-cols-1 sm:grid-cols-2 md:grid-cols-3 gap-4">
            {wishlist.map((item) => {
              const templeId = getTempleIdFromCardId(item.id);
              return (
                <div 
                  key={item.id}
                  onClick={() => {
                    if (templeId) {
                      navigate(`/devotional?section=temples&templeId=${templeId}`);
                    }
                  }}
                  className={`bg-white dark:bg-stone-900 border border-stone-200 dark:border-stone-850 p-5 rounded-[2rem] shadow-sm flex items-center justify-between gap-4 group transition-all ${templeId ? 'cursor-pointer hover:border-amber-500/40 hover:bg-amber-50/10 dark:hover:bg-amber-950/5 hover:scale-[1.01]' : 'hover:border-stone-300 dark:hover:border-stone-700'}`}
                >
                  <div className={`h-12 w-12 rounded-xl flex items-center justify-center shrink-0 text-2xl transition-colors ${templeId ? 'bg-amber-50 dark:bg-amber-950/20 text-amber-600' : 'bg-rose-50 dark:bg-stone-850'}`}>
                    {item.icon}
                  </div>
                  <div className="flex-1 min-w-0">
                    <h5 className="font-gujarati font-black text-base text-stone-850 dark:text-stone-100 truncate flex items-center gap-1.5">
                      <span>{item.name_gu}</span>
                      {templeId && <span className="material-symbols-outlined text-[10px] text-amber-500">open_in_new</span>}
                    </h5>
                    <p className="font-gujarati text-[10px] text-stone-450 dark:text-stone-400 truncate">{item.location}</p>
                  </div>
                  <button
                    onClick={(e) => { e.stopPropagation(); removeFromWishlist(item.id); }}
                    className="h-9 w-9 bg-rose-50 hover:bg-rose-100 dark:bg-stone-800 text-rose-600 rounded-lg flex items-center justify-center active:scale-95 transition-all shrink-0 z-10"
                    title="દૂર કરો"
                  >
                    <span className="material-symbols-outlined text-lg">delete</span>
                  </button>
                </div>
              );
            })}
          </div>
        )}
      </section>
    </div>
  );
}
